public class TesteMediaApi {

	    private String nome;
	    private int alunos;

	    public TesteMediaApi(String nome, int alunos) {
	        this.nome = nome;
	        this.alunos = alunos;
	    }

	    public String getNome() {
	        return nome;
	    }

	    public int getAlunos() {
	        return alunos;
	    }
	}


